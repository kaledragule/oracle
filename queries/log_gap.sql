SELECT LOG_ARCHIVED, LOG_APPLIED , APPLIED_TIME,
(LOG_ARCHIVED-LOG_APPLIED) "LOG_GAP"
FROM
(SELECT MAX(SEQUENCE#)
LOG_ARCHIVED FROM V$ARCHIVED_LOG WHERE DEST_ID=1 AND ARCHIVED='YES' ),
(SELECT MAX(SEQUENCE#)
LOG_APPLIED FROM V$ARCHIVED_LOG WHERE DEST_ID=2 AND APPLIED='YES' ),
(SELECT TO_CHAR(MAX(COMPLETION_TIME),'DD-MON/HH24:MI')
APPLIED_TIME FROM V$ARCHIVED_LOG WHERE DEST_ID=2 AND APPLIED='YES');
